import streamlit as st

st.title('Persona')

st.markdown("""
Persona prompting shapes the model’s tone, style, and perspective by assigning it a specific character or role. This helps generate more consistent and context-aware outputs.

📌 Use Cases:
- Roleplay a specific expert (e.g., “You are a compassionate therapist…”)
- Adjust tone (e.g., “Explain this like a pirate”)
- Create brand-aligned content

🔍 Example:
  “You are a tech-savvy English teacher. Explain the concept of metaphors using examples from Marvel movies.”
""")