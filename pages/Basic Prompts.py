import streamlit as st

st.title('Basic Prompting Techniques')

st.markdown("""
Basic prompting is about clearly instructing the AI on what you want. A good prompt defines the task, provides context, and sets constraints or goals. It often uses direct instructions or examples to guide the model's output.

📌 Tips:
- Be specific about the task: “Summarize this paragraph in 3 bullet points.”
- Set a role or format: “Write a tweet thread about X.”
- Provide examples: “Input: … Output: …”
""")